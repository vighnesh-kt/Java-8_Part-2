package DesignPatterns.Structural.Decorator.FunctionalWay;

public class Coffee {
	
	int price;
	String type;
	
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	
	

}
